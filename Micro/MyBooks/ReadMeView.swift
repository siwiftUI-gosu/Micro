import SwiftUI

struct ReadMeView: View {
    var readMeText = """
    하나 앱 사용법

    1. 앱의 기본 흐름

    - 목표 설정: 매일 단 하나의 목표를 입력할 수 있어요. 하루가 지나면 새로운 목표를 설정할 수 있습니다.
    - 달성 체크: 목표를 달성했다면 버튼을 눌러 완료 상태로 변경하세요. 달성하지 못했다면 그대로 남겨둘 수도 있어요.
    - 목표 모으기: 매일 쌓인 목표들은 "책"으로 묶을 수 있습니다. 책은 여러 날의 목표를 한 번에 정리한 기록이에요.
    - 책 확인: 완성된 책을 언제든 열어 과거의 목표와 달성 여부를 볼 수 있어요.

    2. 사용 방법

        1. 목표 입력하기
            - 앱을 열고 "목표를 작성해주세요"라는 문구 아래에 목표를 입력하세요 (예: "책 10페이지 읽기").
            - 목표는 최대 43자까지 입력 가능하며, 너무 길면 빨간색으로 경고가 표시돼요.
            - 입력 후 "작성 완료" 버튼을 누르면 목표가 저장됩니다.
        2. 목표 달성하기
            - 하루 동안 목표를 이루면 "목표 달성" 버튼을 눌러 완료하세요.
            - 달성하면 "하나의 목표와 함께 내일 다시 뵈어요 ✨" 메시지가 나타나며, 다음 날까지 기다리면 됩니다.
        3. 책으로 만들기
            - 쌓인 목표들을 정리하고 싶을 때 작성중인 “책”을 누르세요.
            - 책 제목을 입력하면(최대 9자), 그동안의 목표들이 하나의 책으로 묶입니다.
            - 중요: 책 제목은 한 번 정하면 수정할 수 없으니 신중히 입력하세요!
        4. 책 확인하기
            - 작성이 완료된 책은 언제나 눌러서 내용을 볼 수 있어요.
            - 책 안에서 전체 목표, 달성한 목표, 미달성 목표를 탭으로 구분해 확인 가능합니다.

    3. 팁

    - 목표는 구체적으로: "운동하기" 대신 "30분 러닝"처럼 명확히 입력하면 달성 여부를 판단하기 쉬워요.
    - 책 제목은 의미 있게: "2025년의 나"처럼 기억에 남을 제목을 짓는 걸 추천해요.
    - 달성 축하: 목표를 달성하면 스스로 작은 보상을 주며 동기부여를 높여보세요!
    """
    
    var body: some View {
        VStack(spacing: 0) {
            BackNavigationBar(title: "ReadMe")
            ScrollView {
                Text(readMeText)
            }
            .padding()

        }
    }
}

